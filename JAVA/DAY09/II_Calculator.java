public class II_Calculator {
  int result = 0;

  int add(int n) {
    result =+ n;
    return result;
  } // 덧셈

  int sub(int n) {
    result -= n;
    return result;
  } // 뺄셈

  int mul(int n) {
    result *= n;
    return result;
  } // 곱셈

  // main은 실제 실행을 위해 있으며 위와 같이 main이 없이 기능(메서드)만으로 구성 되어 있는 것을 '설계도'라고 한다.

  // 
}
